-module(nts_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    nts_metrics:create_metrics(),
    SupPid = nts_sup:start_link(),
    nts_modules_sup:reload(),
    nts_db:start(),
    SupPid.

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

